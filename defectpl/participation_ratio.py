_SPIN_COLORS = {"spin_1": "#1f77b4", "spin_2": "#d62728"}
_SPIN_SYMBOLS = {"spin_1": "spin ↑", "spin_2": "spin ↓"}
_METRIC_LABEL = {"p_ratio": "P-ratio", "ipr": "IPR"}


def _require_matplotlib() -> tuple:
    """Return (matplotlib, pyplot) or raise ImportError with install hint."""
    try:
        import matplotlib as mpl
        import matplotlib.pyplot as plt

        return mpl, plt
    except ImportError:
        raise ImportError(
            "matplotlib is required for PR plotting.  "
            "Install with:  pip install matplotlib"
        )


def _scatter_spin_rows(ax, rows: List[dict], metric: str, x_key: str) -> None:
    """Draw filled/open scatter markers per spin for a list of row dicts."""

    by_spin = {}
    for row in rows:
        by_spin.setdefault(row["spin"], []).append(row)

    for sp_label, sp_rows in sorted(by_spin.items()):
        color = _SPIN_COLORS.get(sp_label, "gray")
        sym = _SPIN_SYMBOLS.get(sp_label, sp_label)
        xs_occ, ys_occ = [], []
        xs_emp, ys_emp = [], []
        for row in sp_rows:
            x = row[x_key]
            y = row[metric]
            if x is None or y is None:
                continue
            if row["occ"] >= 0.5:
                xs_occ.append(x)
                ys_occ.append(y)
            else:
                xs_emp.append(x)
                ys_emp.append(y)

        kw = dict(marker="o", s=35, zorder=3)
        if xs_occ:
            ax.scatter(xs_occ, ys_occ, c=color, label=f"{sym} (occ)", **kw)
        if xs_emp:
            ax.scatter(
                xs_emp,
                ys_emp,
                facecolors="none",
                edgecolors=color,
                label=f"{sym} (empty)",
                **kw,
            )


def plot_pr_vs_energy(
    result: dict,
    metric: str = "p_ratio",
    threshold: float = 0.2,
    vbm: Optional[float] = None,
    cbm: Optional[float] = None,
    emin: Optional[float] = None,
    emax: Optional[float] = None,
    kpt_idx: int = 0,
    title: Optional[str] = None,
    out: Optional[str | Path] = None,
    ax=None,
    figsize: tuple = (8, 5),
    dpi: int = 150,
):
    """
    Scatter plot of P-ratio or IPR versus Kohn-Sham energy.

    Parameters
    ----------
    result : dict
        Nested PR result dict (from :func:`compute_participation_ratios` or
        loaded from ``participation_ratio.json``).
    metric : {"p_ratio", "ipr"}
        Quantity plotted on the Y-axis.
    threshold : float
        Horizontal dashed threshold reference line.  Default 0.2.
    vbm : float, optional
        Valence Band Maximum energy (eV).  Shown as a vertical orange line.
    cbm : float, optional
        Conduction Band Minimum energy (eV).  Shown as a vertical green line.
    emin : float, optional
        Lower energy boundary for filtering.
    emax : float, optional
        Upper energy boundary for filtering.
    kpt_idx : int
        0-based k-point index to use.  Default 0.
    title : str, optional
        Plot title.  Defaults to the defect name in *result*.
    out : str or Path, optional
        If given, save the figure to this path.
    ax : matplotlib.axes.Axes, optional
        Inject into an existing Axes.  When None a new figure is created.
    figsize : tuple
        Figure size (width, height) in inches.
    dpi : int
        Resolution when saving.

    Returns
    -------
    matplotlib.axes.Axes
    """
    _, plt = _require_matplotlib()

    rows = flatten_pr_result(result, kpt_idx=kpt_idx)
    if emin is not None:
        rows = [r for r in rows if r["energy"] is not None and r["energy"] >= emin]
    if emax is not None:
        rows = [r for r in rows if r["energy"] is not None and r["energy"] <= emax]

    standalone = ax is None
    if standalone:
        fig, ax = plt.subplots(figsize=figsize)

    _scatter_spin_rows(ax, rows, metric=metric, x_key="energy")

    ax.axhline(
        threshold,
        color="gray",
        linestyle="--",
        linewidth=1,
        label=f"threshold = {threshold}",
    )

    if vbm is not None:
        ax.axvline(
            vbm,
            color="orange",
            linestyle=":",
            linewidth=1.2,
            label=f"VBM = {vbm:.3f} eV",
        )
    if cbm is not None:
        ax.axvline(
            cbm,
            color="green",
            linestyle=":",
            linewidth=1.2,
            label=f"CBM = {cbm:.3f} eV",
        )

    ax.set_xlabel("Energy (eV)")
    ax.set_ylabel(_METRIC_LABEL.get(metric, metric))
    ax.set_title(title or result.get("defect_name", "defect"))
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    if standalone and out is not None:
        ax.get_figure().tight_layout()
        ax.get_figure().savefig(str(out), dpi=dpi, bbox_inches="tight")
        plt.close(ax.get_figure())

    return ax


